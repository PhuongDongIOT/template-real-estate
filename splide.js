document.addEventListener('DOMContentLoaded', function () {
  const splide = new Splide('#image-carousel', {
    type: 'loop',
    perPage: 3,
    arrows: true,
    pagination: true,
    autoplay: true,
    interval: 3000,
  });

  splide.mount(window.splide.Extensions);
});

